
public class IntValueExample {

	public static void main(String[] args) {
		int ten = 7;
		int two = 0b1000_0101;
		int eight = 077;
		int sixty = 0xFEFE;
		
		System.out.println("10진수 7은:"+ten);
		System.out.println("2진수 10000101은:"+two);
		System.out.println("8진수 77은:"+eight);
		System.out.println("16진수 FEFE은:"+sixty);
	}

}
